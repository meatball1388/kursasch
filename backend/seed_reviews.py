async def run():
    con = await asyncpg.connect(dsn=os.getenv('DB_URL'))

    # Удаляем старые отзывы
    deleted = await con.execute('DELETE FROM reviews')
    print(f'Cleared old reviews')

    # Получаем все активные ресурсы
    resources = await con.fetch('SELECT id FROM resources WHERE is_active = TRUE ORDER BY id')
    print(f'Resources: {[r["id"] for r in resources]}')

    total = 0
    for res in resources:
        rid = res['id']
        # Случайное количество от 30 до 45
        count = random.randint(30, 45)

        # Распределяем рейтинги: 60% пятёрок, 25% четвёрок, 15% троек
        ratings = (
            [5] * int(count * 0.60) +
            [4] * int(count * 0.25) +
            [3] * (count - int(count * 0.60) - int(count * 0.25))
        )
        random.shuffle(ratings)

        names_used = random.sample(NAMES, min(count, len(NAMES)))
        if count > len(names_used):
            names_used += random.choices(NAMES, k=count - len(names_used))

        dates_used = random.choices(DATES, k=count)
        dates_used.sort()

        for j in range(count):
            rating = ratings[j]
            comment = random.choice(COMMENTS_BY_RATING.get(rating, COMMENTS_BY_RATING[4]))
            name = names_used[j]
            date_str = dates_used[j]

            await con.execute(
                '''INSERT INTO reviews (resource_id, author_name, rating, comment, created_at)
                   VALUES ($1, $2, $3, $4, $5)''',
                rid, name, rating, comment,
                date.fromisoformat(date_str)
            )
            total += 1

        real_count = await con.fetchval('SELECT COUNT(*) FROM reviews WHERE resource_id = $1', rid)
        print(f'  Resource {rid}: {real_count} reviews added')

    await con.close()
    print(f'\nTotal reviews inserted: {total}')

asyncio.run(run())
